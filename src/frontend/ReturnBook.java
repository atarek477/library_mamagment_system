    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
     
        if(jTextField1.getText().isEmpty() || jTextField2.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "some fields are empty");

        }
        else{
            String  s1 = jTextField1.getText();
            String  s2 = jTextField2.getText();
            
            Date s3 = jDateChooser1.getDate();

            SimpleDateFormat date1 = new SimpleDateFormat("yyyy-M-dd") ;
          //  String w = s1+","+s2+","+date1.format(s3);
            
            try {
                d1 = librarianRole.returnBook(s1, s2, LocalDate.parse(date1.format(s3)));
                
            } catch (IOException ex) {
                Logger.getLogger(ReturnBook.class.getName()).log(Level.SEVERE, null, ex);
            }
            JOptionPane.showMessageDialog(null, "The student with id "+s1+"should pay a return fee  "+d1+"us dollers for book with id "+s2+" .");
            this.setVisible(false);
            ((JFrame)getParentNode()).setVisible(true);
            ClearFields.clearFields(jTextField1,jTextField2);

        }
    }//GEN-LAST:event_jButton1ActionPerformed

